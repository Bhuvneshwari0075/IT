
public class patternProgram1 {
	public static void main(String[] args) {
		for(int repeat=1;repeat<=5;repeat++)
		{
			for(int star1=1;star1<=repeat;star1++)
            {
	             System.out.print("# ");
            }    
	             for(int star2=repeat;star2<=5;star2++)
	             {
	 	             System.out.print("* ");
	             }
            	
                    
              System.out.println();
		     }
	     }
}
